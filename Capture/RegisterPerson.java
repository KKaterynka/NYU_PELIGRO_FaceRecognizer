    private void displayUserID(){
        // display user id in the upper right corner
        try {
            // connect to db
            connection.connect();
            // extract last entry's id
            connection.executeSQL("SELECT * FROM criminal ORDER BY id DESC LIMIT 1");
            connection.rs.first();
            int idNumber = connection.rs.getInt("id");
            idNumber++;
            txtCriminalID.setText(idNumber + "");
        } catch (SQLException ex) {
            // if error occurred, it means there are 0 entries in db
            // so the id of new entry is 1
            int idNumber = 1;
            txtCriminalID.setText(idNumber + "");
           // Logger.getLogger(RegisterPerson.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
