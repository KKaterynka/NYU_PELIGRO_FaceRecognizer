    private void registerCriminalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerCriminalButtonActionPerformed
        // TODO add your handling code here:

        // extract all the characteristics of criminal
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String dob = txtDOB.getText();
        String age = txtAge.getText();
        String gender = (String) txtGender.getSelectedItem();
        String phoneNumber = txtPhoneNumber.getText();
        String city = txtCity.getText();
        String state = txtState.getText();
        int id = Integer.parseInt(txtCriminalID.getText());

        try {
            if (txtFirstName.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Enter the appropriate first name");
            } else if (txtLastName.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Enter the appropriate last name");
            } else if (txtDOB.getText().trim().split("/")[0].length() != 2 ||
                    txtDOB.getText().trim().split("/")[1].length() != 2 ||
                    txtDOB.getText().trim().split("/")[2].length() != 4) {
                System.out.println(Arrays.toString(txtPhoneNumber.getText().trim().split("-")));
                JOptionPane.showMessageDialog(null, "Enter the appropriate DOB");
            } else if (txtPhoneNumber.getText().trim().split("-")[0].length() != 4 ||
                    txtPhoneNumber.getText().trim().split("-")[1].length() != 4 ||
                    txtPhoneNumber.getText().trim().split("-")[2].length() != 4) {
                JOptionPane.showMessageDialog(null, "Enter the appropriate phone number");
            } else if (txtAge.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Enter the appropriate age");
            } else if (txtCity.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Enter the appropriate city");
            } else if (txtState.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Enter the appropriate state");

            } else {
                CapturePerson personCaptured = new CapturePerson(id, firstName, lastName, dob, age, gender,
                        phoneNumber, city, state);
                personCaptured.setVisible(true);
                personCaptured.setDefaultCloseOperation(DISPOSE_ON_CLOSE);

            }
        } catch (Exception ArrayIndexOutOfBoundsException){
            JOptionPane.showMessageDialog(null, "Fill in with correct values");
        }

        // capture new criminal

    }//GEN-LAST:event_registerCriminalButtonActionPerformed

