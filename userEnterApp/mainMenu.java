    public void setCaptureCard(){
        captureCard.setColors(new Color(204,204,255), new Color(204,204,255));
        captureCard.setTitle("Capture");

        Conn connection = new Conn();
        Integer lastId = null;
        connection.connect();
        connection.executeSQL("SELECT * FROM criminal ORDER BY id DESC LIMIT 1");
        try {
            connection.rs.first();
            String name = connection.rs.getString("first_name") + " " + connection.rs.getString("last_name");
            lastId = connection.rs.getInt("id");
            captureCard.setDescription("Last captured: " + name);
        } catch (SQLException e) {
            captureCard.setDescription("No one was captured");
        }

        dataCard.setDescription("Number of people: " + lastId);
        captureCard.setIcon(new ImageIcon("D:\\icons8-capture-30.png"));
    }

