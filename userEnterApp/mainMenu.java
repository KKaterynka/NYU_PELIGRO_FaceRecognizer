    private void dataCardMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dataCardMouseClicked

        Conn connection = new Conn();
        connection.connect();
        connection.executeSQL("SELECT * FROM criminal");
        ArrayList<ModelOfCriminal> criminals = new ArrayList<>();
        try{
            ResultSet rs = connection.rs;//rs.getInt("id"),
            while (rs.next()){
                ModelOfCriminal criminal = new ModelOfCriminal(rs.getString("first_name"),
                        rs.getString("last_name"), rs.getString("dob"), rs.getString("age"),
                        rs.getString("gender"), rs.getString("phone_number"), rs.getString("city"),
                        rs.getString("state"));
                criminals.add(criminal);
            }
        } catch (SQLException e){

        }


        displayCriminals dbCriminals = new displayCriminals(criminals);
        dbCriminals.setVisible(true);
        dbCriminals.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        dbCriminals.setLocationRelativeTo(null);
        connection.disconnect();
    }//GEN-LAST:event_dataCardMouseClicked

